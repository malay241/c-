#include<iostream>
using namespace std;
int main()
    {
        int a=12;
        cout<<"Size of int "<<sizeof(a)<<endl;
        float b;
        cout<<"Size of float "<<sizeof(b)<<endl;
        char c;
        cout<<"Size of char "<<sizeof(c)<<endl;
        bool d;
        cout<<"Size of boolean "<<sizeof(d)<<endl;
        
        short int s1;
        long long int l1;
        cout<<"Size of short int "<<sizeof(s1)<<endl;
        cout<<"Size of long int "<<sizeof(l1)<<endl;
        return 0;
    }
